export const getCitiesPath = () => {
    const returenedArray = [];
    const dates = getDates(process.env.startDate, process.env.endDate);

    for (let city of cities.slice(0, 2)) {
        dates.forEach(date => {
            returenedArray.push({
                params: {
                    id: city + '-on-' + date
                }
            })
        })
    }
    return returenedArray;
}

export const getDates = (startDate, stopDate) => {
    var dateArray = [];
    var currentDate = moment(startDate);
    var stopDate = moment(stopDate);
    while (currentDate <= stopDate) {
        dateArray.push(moment(currentDate).format('YYYY-MM-DD'))
        currentDate = moment(currentDate).add(1, 'days');
    }
    return dateArray;
}

export const getCitiesByCountryCode = (countryId) => {
    const citiesList = [];
    let splited = [];
    for (let city of cities) {
        splited = city.split('-');
        if (splited[0] === countryId) {
            citiesList.push(city);
        }
    }
    return citiesList;
}